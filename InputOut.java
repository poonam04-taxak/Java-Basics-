//Write a program that reads 3 integers from the user and prints them on separate lines.
import java.util.*;
public class InputOut {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);

        int a=sc.nextInt();
        int b=sc.nextInt();
        int c=sc.nextInt();

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
    sc.close();
}
    
}
