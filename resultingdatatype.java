public class resultingdatatype {
    public static void main(String[] args) {
        byte x=5;
        int y = 6;
        short z = 8;
        float f = 6.44f;
        char c= 'p';
        double d= 67.888;
        int a= y + z;
        float b = f + x;
        int h= x+z;
        int r= c + z;
        double g = f + d;
        System.out.println(a);
        System.out.println(b);
        System.out.println(h);
        System.out.println(r);
        System.out.println(g);


    }
    
}



