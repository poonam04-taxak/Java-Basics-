class Switch
{
    public static void main(String args[]){
        String v="BCA";
        switch(v){
            case "betch":
            System.out.println("you are betch person so you can join iit sector");
            break;
            case "ba":
            System.out.println("sorry you can not join our company");
            break;
            case "BCA":
            System.out.println("you can join our company");
            break;
            default:
            System.out.println("sorry you are not eligible for our company!");
        }
        System.out.println("please join our company if you are eligible!");
    }
}